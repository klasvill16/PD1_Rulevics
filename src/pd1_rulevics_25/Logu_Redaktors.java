    // Testa pieladēšana no Datu bāzes
    private void TestExecutionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TestExecutionButtonActionPerformed
        DataBase db = new DataBase();
        ResultSet rs = null;
        PreparedStatement pst = null;
        String testName = TestList.getSelectedValue();

        JLabel[] questionLabels = {QuestionLabel1, QuestionLabel2, QuestionLabel3, QuestionLabel4, QuestionLabel5};
        JRadioButton[][] answerButtons = {
            {Answer11, Answer12, Answer13},
            {Answer21, Answer22, Answer23},
            {Answer31, Answer32, Answer33},
            {Answer41, Answer42, Answer43},
            {Answer51, Answer52, Answer53}
        };

        // Ja neviens tests nav izvelets
        if (testName == null) {
            showInfo("Tests nav izvelēts!");
            return;
        }
        
        try{
            Connection con = db.connect();
            String sqlCheck = "SELECT * FROM marks WHERE UserName=? AND TestName=? ";
            pst = con.prepareStatement(sqlCheck);
            pst.setString(1, currentUser.username);
            pst.setString(2, testName);
            rs = pst.executeQuery();
            if (rs.next()) {
                showError("Jūms jau ir atzīme par šo testu!");
            }else{
                if (currentUser instanceof Student) {
                    ((Student) currentUser).displayTest(testName, TestNameLabel, questionLabels, answerButtons);
                }
                MainFrameStudent.dispose();
                MainFrameTeacher.dispose();
                TestFrame.setVisible(true);
                TestFrame.pack();
                TestFrame.setLocationRelativeTo(null);
            }
        }catch (Exception e) {
            System.out.println(e.getMessage());
        }
        

    }//GEN-LAST:event_TestExecutionButtonActionPerformed

